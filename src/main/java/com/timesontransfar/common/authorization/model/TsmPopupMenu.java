package com.timesontransfar.common.authorization.model;

public class TsmPopupMenu {
	
	private String menuId;

	private String menuName;

	private String url;

	private String menuSeq;

	private String funcCode;

	private String isMenuitem;

	private String isTopMenu;

	private String menuGroup;

	public String getMenuId() {
		return menuId;
	}

	public void setMenuId(String menuId) {
		this.menuId = menuId;
	}

	public String getMenuName() {
		return menuName;
	}

	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getMenuSeq() {
		return menuSeq;
	}

	public void setMenuSeq(String menuSeq) {
		this.menuSeq = menuSeq;
	}

	public String getFuncCode() {
		return funcCode;
	}

	public void setFuncCode(String funcCode) {
		this.funcCode = funcCode;
	}

	public String getIsMenuitem() {
		return isMenuitem;
	}

	public void setIsMenuitem(String isMenuitem) {
		this.isMenuitem = isMenuitem;
	}

	public String getIsTopMenu() {
		return isTopMenu;
	}

	public void setIsTopMenu(String isTopMenu) {
		this.isTopMenu = isTopMenu;
	}

	public String getMenuGroup() {
		return menuGroup;
	}

	public void setMenuGroup(String menuGroup) {
		this.menuGroup = menuGroup;
	}
	
}
