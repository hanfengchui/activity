package com.timesontransfar.customservice.workFlowOrg.pojo;

public class WorkSheetRule {

	private int ruleId;//规则ID
	private String reuleName;//规则名
	private String reuleDesc;//规则描述
	private int ruleType;//规则类型
	private String pkgEffDate;//生效时间
	private String pkgExpDate;//实效时间
	/**
	 * @return pkgEffDate
	 */
	public String getPkgEffDate() {
		return pkgEffDate;
	}
	/**
	 * @param pkgEffDate 要设置的 pkgEffDate
	 */
	public void setPkgEffDate(String pkgEffDate) {
		this.pkgEffDate = pkgEffDate;
	}
	/**
	 * @return pkgExpDate
	 */
	public String getPkgExpDate() {
		return pkgExpDate;
	}
	/**
	 * @param pkgExpDate 要设置的 pkgExpDate
	 */
	public void setPkgExpDate(String pkgExpDate) {
		this.pkgExpDate = pkgExpDate;
	}
	/**
	 * @return reuleDesc
	 */
	public String getReuleDesc() {
		return reuleDesc;
	}
	/**
	 * @param reuleDesc 要设置的 reuleDesc
	 */
	public void setReuleDesc(String reuleDesc) {
		this.reuleDesc = reuleDesc;
	}
	/**
	 * @return reuleName
	 */
	public String getReuleName() {
		return reuleName;
	}
	/**
	 * @param reuleName 要设置的 reuleName
	 */
	public void setReuleName(String reuleName) {
		this.reuleName = reuleName;
	}
	/**
	 * @return ruleId
	 */
	public int getRuleId() {
		return ruleId;
	}
	/**
	 * @param ruleId 要设置的 ruleId
	 */
	public void setRuleId(int ruleId) {
		this.ruleId = ruleId;
	}
	/**
	 * @return ruleType
	 */
	public int getRuleType() {
		return ruleType;
	}
	/**
	 * @param ruleType 要设置的 ruleType
	 */
	public void setRuleType(int ruleType) {
		this.ruleType = ruleType;
	}
	
	
	
}
